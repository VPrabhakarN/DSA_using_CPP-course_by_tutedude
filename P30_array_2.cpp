// C++ program to demonstrate the array 
#include<iostream>
using namespace std;

int main(){
    // defining an array 
    int a[5] = {12, 13, 14, 15, 16};

    // accessing one element at one time 
    cout << a[0] << endl;
    cout << a[1] << endl;
    cout << a[2] << endl;
    cout << a[3] << endl;
    cout << a[4] << endl;
    return 0;
}